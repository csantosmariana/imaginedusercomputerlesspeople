$(document).mousemove(function(e){
            const height = $('#image1').height() / 10;
            const width = $('#image1').width() / 28;
                $("#image1").css({
                left:e.pageX-width, 
                top:e.pageY-height
                });
            });

            $('.link1').mouseenter(_=> {
                $('#image1').show();
            });
  
            $('.link1').mouseleave(_=> {
                $('#image1').hide();
            });

